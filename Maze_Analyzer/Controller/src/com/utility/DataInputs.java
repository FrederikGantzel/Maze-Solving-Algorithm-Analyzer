package com.utility;

public class DataInputs {
    public int SmallestMaze;
    public int LargestMaze;
    public int repetitionsMaze;
    public long itteWeight;
    public long timeWeight;

    public DataInputs (int SmallestMaze, int LargestMaze, int repetitionsMaze, long itteWeight, long timeWeight) {
        this.SmallestMaze = SmallestMaze;
        this.LargestMaze = LargestMaze;
        this.repetitionsMaze = repetitionsMaze;
        this.itteWeight = itteWeight;
        this.timeWeight = timeWeight;
    }
}
